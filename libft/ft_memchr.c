#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t		i;

	if (c > 255)
		c = c % 256;
	i = 0;
	while (i < n)
	{
		if (((const char *) s)[i] == c)
			return ((void *) &s[i]);
		i++;
	}
	return (NULL);
}
